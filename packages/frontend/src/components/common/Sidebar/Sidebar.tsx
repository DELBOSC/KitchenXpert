  'flex items-center gap-3 px-4 py-2.5 rounded-lg text-sm font-medium transition-colors';
const linkActiveClass = 'bg-blue-50 dark:bg-blue-900/30 text-blue-600 dark:text-blue-400';
const linkInactiveClass =
  'text-gray-600 dark:text-gray-300 hover:bg-gray-100 dark:hover:bg-gray-700';

export default function Sidebar({ isOpen, onClose }: SidebarProps): React.ReactElement {
  const { user } = useAuth();
  const { t } = useTranslation();
  const isAdmin = user?.role === 'admin';

  const filteredItems = navItems.filter((item) => !item.adminOnly || isAdmin);

  return (
    <>
      {/* Overlay mobile */}
      {isOpen && (
        <div
          className="fixed inset-0 bg-black/50 z-40 lg:hidden"
          onClick={onClose}
          role="presentation"
        />
      )}

      {/* Sidebar */}
      <aside
        aria-label={t('nav.navigation')}
        className={`fixed top-16 left-0 bottom-0 w-64 bg-white dark:bg-gray-800 border-r border-gray-200 dark:border-gray-700 z-40 transform transition-transform duration-200 ease-in-out ${
          isOpen ? 'translate-x-0' : '-translate-x-full'
        } lg:translate-x-0 lg:static lg:z-auto overflow-y-auto`}
      >
        <nav className="p-4 space-y-1">
          {/* Section principale */}
          <div className="mb-4">
            <p className="px-4 text-xs font-semibold text-gray-400 dark:text-gray-500 uppercase tracking-wider mb-2">
              {t('nav.navigation')}
            </p>
            {filteredItems
              .filter((i) => !i.adminOnly)
              .map((item) => (
                <NavLink
                  key={item.to}
                  to={item.to}
                  onClick={onClose}
                  className={({ isActive }) =>
                    `${linkBaseClass} ${isActive ? linkActiveClass : linkInactiveClass}`
                  }
                >
                  {item.icon}
                  {t(item.labelKey)}
                </NavLink>
              ))}
          </div>

          {/* Section admin */}
          {isAdmin && (
            <div className="pt-4 border-t border-gray-200 dark:border-gray-700">
              <p className="px-4 text-xs font-semibold text-gray-400 dark:text-gray-500 uppercase tracking-wider mb-2">
                {t('nav.administration')}
              </p>
              {filteredItems
                .filter((i) => i.adminOnly)
                .map((item) => (
                  <NavLink
                    key={item.to}
                    to={item.to}
                    onClick={onClose}
                    className={({ isActive }) =>
                      `${linkBaseClass} ${isActive ? linkActiveClass : linkInactiveClass}`
                    }
                  >
                    {item.icon}
                    {t(item.labelKey)}
                  </NavLink>
                ))}
            </div>
          )}
        </nav>
      </aside>
    </>
  );
}
